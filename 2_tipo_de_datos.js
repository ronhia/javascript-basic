
// TIPO DE DATOS

// String (Textos)
let myString1 = String("Carlos") // Cuando especifica el tipo de dato "String"
let myString2 = "Rony" // Forma standar
let myString3 = myString1 + " " + myString2 // Forma de concatenar los String (Forma standar)
let myString4 = myString1.concat(" ").concat(myString2) // Otra forma de concatenar los string usando "concat"

console.log(myString3)
console.log(myString4)


// Int (Numero enteros)
