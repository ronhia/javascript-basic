let myInt5 = myInt1 * myInt2 // Multiplici칩n
let myInt6 = myInt1 / myInt2 // Divisi칩n
let myInt7 = myInt1 % myInt2 // Residuo de la divisi칩n

console.log(myInt3)
console.log(myInt4)
console.log(myInt5)
console.log(myInt6)
console.log(myInt7)


